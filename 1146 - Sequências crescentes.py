# -*- coding: utf-8 -*-
A=0
Sd=[]
while A==0:
    B=int(input())
    st=""
    if B==0:
        A=0
        break
    for i in range(1,B+1,1):
        if i!=B:
            st+=str(i)+" "
        if i==B:
            st+=str(i)
            
    Sd.append(st)

for i in Sd:
    print(i)
    
        
        
        
    
