while True:
    try:
        
        ent=input().split(":")
        horaSaida=int(ent[0])
        minSaida=int(ent[1])

        qtdMinHoras=horaSaida*60
        qtdTotalMin=qtdMinHoras+minSaida


        atrasoMaxOudif=((qtdTotalMin+60)-(8*60))
        if atrasoMaxOudif>0:
            print("Atraso maximo: %d"% atrasoMaxOudif)
        else:
            print("Atraso maximo: 0")
            
    except:
        break
    
