# -*- coding: utf-8 -*-
listaImpares=[]
listaPares=[]
listaPosi=[]
listaNeg=[]
for i in range(5):
    valor=int(input())
    if valor%2==0:
        listaPares.append(valor)
    else:
        listaImpares.append(valor)
    if valor>0:
        listaPosi.append(valor)
    if valor<0:
        listaNeg.append(valor)

TamPares=len(listaPares)
TamImpares=len(listaImpares)
TamPosi=len(listaPosi)
TamNeg=len(listaNeg)
print("""%d valor(es) par(es)
%d valor(es) impar(es)
%d valor(es) positivo(s)
%d valor(es) negativo(s)""" %(TamPares,TamImpares,TamPosi,TamNeg))
